// switch statements
var grade = "C";

switch(grade) {
	case 'A':
		console.log('Awesome job');
		break;

	case 'B':
		console.log('Good job');
		break;

	case 'C':
		console.log('Okay job');
		break;

	case 'D':
		console.log('Not so good job');
		break;

	case 'F':
		console.log('Bad job');
		break;

	default:
		console.log('Unexpected grade value entered');
}